use App\Models\User;
use Illuminate\Support\Str;

class UserObserver
{
    /**
     * Handle the User "created" event.
     */
    public function created(User $user): void
    {
        if (empty($user->username)) {
            do {
                $user->username = 'user_' . Str::random(8);
            } while (User::where('username', $user->username)->exists());
        }

        $user->save();
    }

